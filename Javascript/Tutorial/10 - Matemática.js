// A classe Math tem métodos e propriedades relacionada a matemática

// Operadores matemáticos - Todos precisam de dois operandos

// Adição (+) - soma da esquerda com da direita
// Subtração (-) - Diferença entre o número da esquerda com da direita
// Multiplicação (*) - Produto de dois valores
// Divisão (/) - Divide o da esquerda com o da direita
// Resto (%) - Divide o da esquerda com o da direita, e retorna o resto

// Potência e raiz quadrada

// Math.pow(x, y); - Potência da base x, e expoente y.
// Math.sqrt(x); - Raiz quadrada de x

// Outras operações matemáticas

// Math.abs(x); - Número absoluto de X, o módulo |x|
// Math.sin(x); - Seno de x, o ângulo deve ser em radianos
// Math.cos(x); - Cosseno do x, o ângulo deve ser em radianos
// Math.tan(x); - Tangente de x, o ângulo deve ser em radianos
// Math.cbrt(x); - Raiz Cúbica de x
// Math.log10(X); - Logarítmo de base 10 de X
// Math.log(x); - Logarítmo natural de X
// Math.sign(x); - Retorna o sinal de x (positivo, zero, negativo)
//
// Propriedades

// Math.E - Euler
// Math.pi - PI
// Math.SQRT2 - Raiz quadrada de 2

// Minimo e Máximo

// Math.max(...x); - Maior número
// Math.min(...x); - Menor número

// Número Aleatório

// Math.random(); - Número aleatório entre 0 e 1

// Arrendondamento

// Math.floor(); - Retorna o número para o inteiro mais perto (para baixo);
// Math.ceil(); - Redonda o número para o inteiro mais perto (para cima);
// Math.trunc(); - Corta a parte decimal do número
