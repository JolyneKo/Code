// O Operador Ternário, simplifica a estrutura if-else para uma linha
// O Operador Ternário retorna um valor que é guardado numa variável
// O Operador Ternário tem a seguinte estrutura

// (condição) ? true : false;

// Condição: Condição a ser avaliada
// ?: Separa os valores de retorno da condição
// true: valor retornado se verdadeiro
// false: valor retornado se falso
// : - separa os valores de retorno

// O Operador Ternário avalia uma expressão, e retorna um valor caso ela for verdadeira, outro valor caso for falsa

var x = 5;
var y = 6;

var resultado = (x > y) ? "X maior que Y" : "X menor do que Y";

console.log(resultado); // "X menor do que Y"
