// JSON - JavaScript Object Notation

// É usado para transportar dados
// Várias línguages podem ler JSON
// Arquivos '.json' usam a notação de objeto, e são parecidos com ele
// A diferença, que não aceita funções e as propriedades são escritas em string de áspas duplas

// JSON aceita: String, Number, Boolean, Object, Array

// Exemplo Arquivo JSON:
//
// {
//  "nome": "João",
//  "idade": 23,
//  "moradia": {
//      "cidade": "Belem",
//      "estado": "Pará",
//      "país": "Brasil"
//  },
//  "hobbies": ["beber", "jogar", "dormir"]
// }
//
// Para transformar um objeto em JSON, usa o método
// JSON.stringify();
//
// Para transformar JSON em objeto, usa o método
// JSON.parse();
