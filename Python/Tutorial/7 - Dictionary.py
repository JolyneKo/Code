# Dicionário é uma lista não ordenada, que guarda pares de chave e valor
# Um par de chave de valor, guarda um valor á um nome.
# Idade: 16

# Dicionários guardam vários pares. E podem ser acessados usando a chave de cada par.

lista_de_compras = {
    "Banana": 1,
    "Abacaxi": 1,
    "Batata": 5
}

lista_de_compras["Abacaxi"]  # 1

# É possível adicionar um elemento usando a notação dos colchetes

lista_de_compras["Cajú"] = 6
