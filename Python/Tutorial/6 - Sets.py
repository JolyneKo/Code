# Sets é uma sequência de elementos, não ordenados, e não permite itens duplicados.
# Não é possível acessar um elemento usando o index
# Não é possível modificar os elementos de um set, somente adicionar e remover.

# Sets são representados por {} chaves

sets = {1, 2, 3, 4, 5}

# É possível adicionar elementos a um set usando

sets.add(6)  # {1, 2, 3, 4, 5, 6}

# E remover usando

sets.remove(1)  # {2, 3, 4, 5, 6}
