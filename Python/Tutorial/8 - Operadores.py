# Operadores Aritméticos

# Os operadores aritméticos precisam de 2 operandos, e o operador no meio.

# 2 + 2

# O Python tem 7 operadores

# +  Adição
# -  Subtração
# *  Multiplicação
# /  Divisão
# %  Módulo (resto)
# // Floor division
# ** Exponenciação

# Adição - Soma os operandos
# Subtração - Subtrai os operandos (diferença)
# Multiplicação - Produto entre os operandos
# Divisão - Divisão entre operandos (retorna float)
# Módulo - Divide os operados, e retorna o resto.
# Floor division - Divide os operandos (retorna int)
# Exponenciação - Potência entre dois operandos
