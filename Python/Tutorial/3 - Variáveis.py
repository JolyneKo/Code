# Python tem tipo dinâmico, não é preciso específicar o tipo. Ele automáticamente sabe.
# As variáveis podem guardar díferentes tipos de dados.

# As variáveis são sensível ao caso.

# String
# Int
# Float
# Bool
# Lists
# Tuples
# Sets
# Dictionaries

# Para verificar o tipo de uma variável, usa a função type()

type("Hello")  # Str

# Operador de atribuíção

