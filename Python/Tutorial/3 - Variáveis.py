# Regras de nomes de variáveis

# Precisam começar com underscore (_) ou uma letra
# Usam snake_case
# Não podem conter número no início, somente no meio/fim
# Único caractére especial, é o underscore (_)

# Multiplos valroes podem ser atribuídos a várias variáveis

